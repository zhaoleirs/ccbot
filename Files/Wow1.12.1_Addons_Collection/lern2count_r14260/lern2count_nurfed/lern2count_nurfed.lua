		local Button = getglobal(TButtonName)

		if(Button and Button:IsVisible()) then
			local AId = ActionButton_GetPagedID(Button)
			table.insert(ltc.CountList,{[TButtonName.."count"] = AId})
			--ltc:Print(TButtonName)
		end
	end
end

